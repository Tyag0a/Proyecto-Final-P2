package co.edu.uniquindio.marketplace.model;

public class Persona {
    String nombre;
    String apellido;
    String cedula;
    String direccion;

    public Persona(String nombre, String apellido, String cedula, String direccion) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.cedula = cedula;
        this.direccion = direccion;
    }
}
